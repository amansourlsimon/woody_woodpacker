#include <string.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned int			i;
	const unsigned char		*s1_c;
	const unsigned char		*s2_c;

	i = 0;
	s1_c = s1;
	s2_c = s2;
	while (i < n)
	{
		if (s1_c[i] != s2_c[i])
			return (s1_c[i] - s2_c[i]);
		i++;
	}
	return (0);
}
